print('*** Matrix Equation with list: aa * xx = bb; find bb ***')

# =========================================================
## --- setting matrix equation --- ##
aa1 = [ [1., 1., 1., 1.], [1., 2., 1., 1.],
        [1., 1., 3., 1.], [1., 1., 1., 4.] ]
aa2 = [ [1., 1., 1., 1.], [1., 1., 2., 1.],
        [1., 3., 1., 1.], [4., 1., 1., 1.] ]
xx = [1., 2., 3., 4.]
bb = [0., 0., 0., 0.]
aa = aa1
rank = len(bb)

# =========================================================
## --- main process --- ##
for i in range(rank):
    bb[i] = 0
    for j in range(rank):
        bb[i] += aa[i][j] * xx[j]
print(f'{aa = }\n{xx = }\n{bb = }')

