# -----------------------------------------------------------------------------
number = 360
prime_factors = []  # 'list' of factors
num, fac = number, 2
while num > 1:
    if num % fac == 0:
        prime_factors.append(fac)
        num //= fac
    else:
        fac += 1
print(f'{number = }')
print(f'{prime_factors = }')

